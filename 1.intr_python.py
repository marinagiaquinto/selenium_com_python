num_1 = int(input('Escreva um número inteiro: '))
num_2 = int(input('Escreva mais um número inteiro: '))
num_3 = float(input('Escreva um número decimal: '))

# o produto do dobro do primeiro com a metade do segundo

res_1 = ((2 * num_1) * (num_2/2))
print(f'O produto do dobro do primeiro com a metade do segundo é: {res_1}')

# a soma do triplo do primeiro com o terceiro

res_2 = ((3 * num_1) + num_3)
print(f'A soma do triplo do primeiro com o terceiro é: {res_2}')

# o terceiro elevado ao cubo
res_3 = (num_3**3)
print(f'O terceiro elevado ao cubo é: {res_3}')


